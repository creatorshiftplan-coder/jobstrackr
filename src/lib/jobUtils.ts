/**
 * Extracts the first matching Indian state/UT/city from a text string.
 * Returns null if none found.
 */
export function extractLocationFromText(text: string): string | null {
    const lower = text.toLowerCase();
    // Check states
    for (const entry of INDIAN_LOCATION_DATA) {
        if (lower.includes(entry.state.toLowerCase())) return entry.state;
    }
    // Check districts
    for (const entry of INDIAN_LOCATION_DATA) {
        for (const district of entry.districts) {
            if (lower.includes(district.toLowerCase())) return district;
        }
    }
    // Check job hubs
    for (const entry of INDIAN_LOCATION_DATA) {
        for (const hub of entry.job_hubs) {
            if (lower.includes(hub.toLowerCase())) return hub;
        }
    }
    return null;
}

const ALL_INDIA_LOCATION_PATTERN = /\ball india\b|\bacross india\b|\bpan india\b|\bvarious\b|\bnationwide\b|\bindia\b/i;

export function isAllIndiaLocationText(location: string | null | undefined): boolean {
    const normalized = cleanLocationString(location).toLowerCase();
    if (!normalized) return false;
    // If the text resolves to a concrete state (via state/district/hub), do not treat it as All India.
    if (resolveStateFromLocationText(normalized)) return false;
    return ALL_INDIA_LOCATION_PATTERN.test(normalized);
}

// Resolves a state from any location text by checking state names, district names, and job hubs.
export function resolveStateFromLocationText(location: string | null | undefined): string | null {
    const normalized = cleanLocationString(location).toLowerCase();
    if (!normalized) return null;

    for (const entry of INDIAN_LOCATION_DATA) {
        if (normalized.includes(entry.state.toLowerCase())) return entry.state;
    }

    for (const entry of INDIAN_LOCATION_DATA) {
        for (const district of entry.districts) {
            if (normalized.includes(district.toLowerCase())) return entry.state;
        }
    }

    for (const entry of INDIAN_LOCATION_DATA) {
        for (const hub of entry.job_hubs) {
            if (normalized.includes(hub.toLowerCase())) return entry.state;
        }
    }

    return null;
}

