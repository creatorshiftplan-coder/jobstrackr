/**
 * Cleans up a location string by removing trailing numbers, special chars, or gibberish.
 * E.g., 'Hyderabad2812' -> 'Hyderabad'
 */
export function cleanLocationString(loc: string | null | undefined): string {
    if (!loc) return '';
    // Remove trailing numbers, parentheses, and non-alpha chars after a word
    // E.g., Hyderabad2812) -> Hyderabad
    return loc.replace(/[^a-zA-Z\s,]+.*$/, '').trim();
}

